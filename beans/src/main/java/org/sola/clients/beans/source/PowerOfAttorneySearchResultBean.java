package org.sola.clients.beans.source;

/**
 * Represents search result of Power of attorney. Could be populated 
 * from the {@link PowerOfAttorneySearchResultTO} object.<br /> 
 */
public class PowerOfAttorneySearchResultBean extends SourceSearchResultBean {
    private String attorneyName;
    private String personName;
    
    public PowerOfAttorneySearchResultBean(){
        super();
    }

    public String getAttorneyName() {
        return attorneyName;
    }

    public void setAttorneyName(String attorneyName) {
        this.attorneyName = attorneyName;
    }

    public String getPersonName() {
        return personName;
    }

    public void setPersonName(String personName) {
        this.personName = personName;
    }
}
